# 1. Definimos a função que saiba somar
def somar(a, b):
    resultado = a + b
    print(f"A soma de {a} e {b} é igual a {resultado}")

# 2. Criamos as variáveis com os valores
numero1 = 10
numero2 = 5

# 3. Chamamos a função (não precisamos guardar o resultado, pois ele é exibido diretamente)
somar(numero1, numero2)

